#include<stdio.h>

//This program will print the message 'Breaking Barriers'
int main() 
{
	//Initialize a counter to control the loop
	int i; 
	
	//Loop through 2000 times
	for (i = 0; i < 2000; i++) 
	{
		//Print the message
		printf("Breaking Barriers\n"); 
	}
	
	//Terminate the program
	return 0; 
}